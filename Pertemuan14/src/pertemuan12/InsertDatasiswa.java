    private void loadComboKelas() {
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("Pertemuan12PU");
        EntityManager em = emf.createEntityManager();

        cbKelas.removeAllItems();

        try {
            java.util.List<Kelas> list = em.createNamedQuery("Kelas.findAll", Kelas.class).getResultList();

            for (Kelas k : list) {
                cbKelas.addItem(k.getNamaKelas());   // masukkan objek KELAS
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal load kelas: " + e.getMessage());
        }
    }

