    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtabsen.getText().isEmpty()
                || txtnama.getText().isEmpty()
                || txtwalkel.getText().isEmpty()
                || txtwalmur.getText().isEmpty()
                || cbKelas.getSelectedItem() == null) {

            JOptionPane.showMessageDialog(this, "Isi semua data terlebih dahulu!");
            return;
        }

        try {
            int absen = Integer.parseInt(txtabsen.getText());

            em.getTransaction().begin();

            // Ambil nama kelas dari combo
            String namaKelas = cbKelas.getSelectedItem().toString();

            // Cari kelas berdasarkan nama
            Kelas kelas = em.createQuery(
                    "SELECT k FROM Kelas k WHERE k.namaKelas = :nama", Kelas.class)
                    .setParameter("nama", namaKelas)
                    .getSingleResult();

            Datasiswa murid = new Datasiswa();
            murid.setAbsen(txtabsen.getText());
            murid.setNama(txtnama.getText());
            murid.setWaliKelas(txtwalkel.getText());
            murid.setWaliMurid(txtwalmur.getText());
            murid.setIdKelas(kelas); // foreign key

            em.persist(murid);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Data berhasil disimpan ke database!");
            bersih();
            dispose();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Absen harus berupa angka!");
        } catch (Exception e) {
            if (em.getTransaction().isActive()) {
                em.getTransaction().rollback();
            }
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data: " + e.getMessage());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

